#include "libft.h"

int	ft_memcmp(const void *str1, const void *str2, size_t count)
{
	int				i;
	unsigned char	*s1;
	unsigned char	*s2;

	s1 = (unsigned char *)str1;
	s2 = (unsigned char *)str2;
	i = 0;
	while (count && (s1[i] == s2[i]))
	{
		i++;
		count--;
	}
	if (count == 0)
		return (0);
	return (s1[i] - s2[i]);
}
